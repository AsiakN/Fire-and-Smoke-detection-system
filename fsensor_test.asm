ORG 00H

E EQU P3.2
RS EQU P3.4
RW EQU P3.3

PMP EQU P1.1


GSENS EQU P1.2
FSENS EQU P1.0
BULB EQU P0.1
BUZZER EQU P1.3

SETB GSENS

MAIN:

	
	CLR P1.0 
	CLR P1.2 
	JNB P1.0, FSENSORON; if no bit activate pump
	JNB P1.2, GSENSORON
	ACALL MAIN
	

FSENSORON:
	
	SETB PMP
	SETB BUZZER
	SETB BULB
	ACALL MAIN
	
GSENSORON:

         SETB PMP
         CLR  BUZZER
         SETB BULB
         ACALL MAIN


END
